//++++++++++++++++++++++++| BEYGJA EFTIR BRAUT |+++++++++++++++++++++++++++++
void driveForward(int forwardtime)
{
		while(SensorValue[leftEncoder] > forwardtime)  // While less than 5 rotations on the leftEncoder...
  	{
  			if(SensorValue[rightEncoder] == SensorValue[leftEncoder])
  			{
  				motor[rightMotor] = 60;
  				motor[leftMotor] = 60;
  			}
  			else if(SensorValue[rightEncoder] > SensorValue[leftEncoder])
  			{
  				motor[rightMotor] = 60;
  				motor[leftMotor] = 80;
  			}
  			else
  			{
  				motor[rightMotor] = 80;
  				motor[leftMotor] = 60;
  			}
  	}
}

void clearEncoder()
{
	SensorValue[rightEncoder] = 0;
	SensorValue[leftEncoder] = 0;
}

void stopMotor()
{
	motor[rightMotor] = 0;
 	motor[leftMotor]  = 0;
 	wait1Msec(1000);  // 1 Second Delay
}

void rightTurn90()
{
	motor[leftMotor] = 60;
	motor[rightMotor]= -60;

	 while(SensorValue[rightEncoder] < 207 || SensorValue[leftEncoder] > -207)
	 {
    if(SensorValue[rightEncoder] > 207) {motor[rightMotor] = 0;}
    if(SensorValue[leftEncoder] < -207) {motor[leftMotor] = 0;}
   }
}
void leftTurn90()
{
	motor[leftMotor] = -60;
	motor[rightMotor]= 60;

	 while(SensorValue[rightEncoder] > -207 || SensorValue[leftEncoder] < 207)
	  {
    if(SensorValue[rightEncoder] > 207) {motor[rightMotor] = 0;}
    if(SensorValue[leftEncoder] < -207) {motor[leftMotor] = 0;}
    }
}


task drive()
{
			clearEncoder();

			driveForward(-564);

			stopMotor();

 			clearEncoder();

 			leftTurn90();

			stopMotor();

 			clearEncoder();

 			driveForward(-564);

 			stopMotor();

 			clearEncoder();

 			rightTurn90();

 			stopMotor();

 			clearEncoder();

 			driveForward(-564);

 			stopMotor();

 			clearEncoder();

 			rightTurn90();

 			stopMotor();

 			clearEncoder();

 			driveForward(-564);
}

//+++++++++++++++++++++++++++++++++++++++++++++| MAIN |+++++++++++++++++++++++++++++++++++++++++++++++
task main ()
{
	int run = 1;
	while(run == 1)
	{
		motor[leftMotor]  = (vexRT[Ch2] + vexRT[Ch1])/2;  // (y + x)/2
		motor[rightMotor] = (vexRT[Ch2] - vexRT[Ch1])/2;  // (y - x)/2

// Raise, lower or do not move arm
		if(vexRT[Btn5U] == 1)       	//If button 5U is pressed...
		{
			motor[armMotor] = 127;    	//...raise the arm.
		}
		else if(vexRT[Btn5D] == 1)  	//Else, if button 5D is pressed...
		{
			motor[armMotor] = -127;   	//...lower the arm.
		}
		else                      		//Else (neither button is pressed)...
		{
			motor[armMotor] = 0;      	//...stop the arm.
		}

// Open, close or do not more claw
		if(vexRT[Btn6U] == 1)       	//If Button 6U is pressed...
		{
			motor[clawMotor] = 127;  		//...close the gripper.
		}
		else if(vexRT[Btn6D] == 1)  	//Else, if button 6D is pressed...
		{
			motor[clawMotor] = -127; 		//...open the gripper.
		}
		else                      		//Else (neither button is pressed)...
		{
			motor[clawMotor] = 0;    		//...stop the gripper.
		}

		if(vexRT[Btn8U] == 1)
		{
				StopTask(drive);
		}
		if(vexRT[Btn8D] == 1)
		{
					StartTask(drive);
		}
	}
}
